function renderlist(){
    if(list.length>0){
        var html = '';
        for (let i = 0; i< list.length; i++){
            html+=`<div class="list-item">
                    <div class="list-left">
                        <img src="${list[i].img}" alt="">
                        <div class="list-detail">
                            <p style="font-size: 1.7vw;">${list[i].name}</p>
                            <p style="font-size: 1.4vw;">${numberWithCommas(list[i].price*list[i].count)} Baht</p>
                        </div>
                    </div>
                    <div class="list-right">
                        <p onclick="deinitems('-',${i})" class="btn-control">-</p>
                        <p id="countitems${i}" style="margin: 0 10px;">${list[i].count}</p>
                        <p onclick="deinitems('+',${i})" class="btn-control">+</p>
                    </div>
                </div>`;
        }
        $('#mylist').html(html)
    }
    else{
        $('#mylist').html(`<p>Not found product</p>`)
    }
}

function deinitems(action,index){
    if(action == '-'){
        if(list[index].count > 0){
            list[index].count--;
            $("#countitems"+index).text(list[index].count);

            if(list[index].count <=0){
                swal.fire({
                    icon: 'warning',
                    title: 'Are you sure to delete',
                    showConfirmButton: true,
                    showCancelButton: true,
                    confirmButtonText: 'Delete',
                    cancelButtonText: 'Cancel'
                }).then((res) => {
                    if(res.isConfirmed){
                        list.splice(index,1);
                        console.log(list)
                        renderlist();
                        $("#listcount").css('display','flex').text(list.length);
                        if(list.length <= 0){
                            $("#listcount").css('display','none');
                        }
                    }
                    else{
                        list[index].count++;
                        $("#countitems"+index).text(list[index].count);
                        renderlist();
                    }
                })
            }
            renderlist();
        }
    }
    else if(action == '+'){
        list[index].count++;
        $("#countitems"+index).text(list[index].count);
        renderlist();
    }
}
